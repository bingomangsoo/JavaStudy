package Excercise;

public class excercise14 {

	public static void main(String[] args) {
		
		String rsp = "가위";
		
		switch (rsp) {
		case "가위" : System.out.println(1);
		break;
		case "바위" : System.out.println(2);
		break;
		case "보" : System.out.println(3);
		
		default : System.out.println(0);
		}

	}

}
